number_one = int(input("Enter number one: "))
number_two = int(input("Enter number two: "))
number_three = int(input("Enter number three: "))

min_number = min(number_one, number_two, number_three)
max_number = max(number_one, number_two, number_three)

print("Max number: " + str(max_number))
print("Min number: " + str(min_number))
